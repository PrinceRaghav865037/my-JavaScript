// while and do while loop 
let index = 0;
while(index<=10){
    console.log(`value of index is ${index}`)
    index += 2
}
let arr = [9, 6 , 3, 10]
let i = 0;
while(i<arr.length){
    console.log(`value at index ${i} is ${arr[i]}`)
    i++
}
let score = 11;
do{
    console.log(score);
    score++;
}while(score<=10)